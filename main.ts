function ranking () {
    pause(100)
    jugador = game.askForString("Intro tus iniciales (4)", 4)
    if (blockSettings.exists(jugador)) {
        if (blockSettings.readNumber(jugador) > tiempo) {
            blockSettings.writeNumber(jugador, info.score())
            game.splash("Enhorabuena " + jugador, "Nuevo record: " + tiempo + " seg")
        } else {
            blockSettings.writeNumber(jugador, info.score())
            game.splash("Lograste terminar " + jugador, "Tu puntuación: " + tiempo + " seg")
        }
    } else {
        blockSettings.writeNumber(jugador, info.score())
        game.splash("Bienvenido " + jugador, "Tu puntuación: " + tiempo + " seg")
    }
}
